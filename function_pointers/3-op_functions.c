#include "3-calc.h"
/**
 * op_add - sum a and b
 * @a: integer
 * @b: integer
 * Return: sum
 */
int op_add(int a, int b)
{
return (a + b);
}
/**
 * op_sub - sub a and b
 * @a: integer
 * @b:integer
 * Return: difference
 */
int op_sub(int a, int b)
{
return (a - b);
}
/**
 * op_mul - multiply a and b
 * @a: integer
 * @b: integer
 * Return: product
 */
int op_mul(int a, int b)
{
return (a * b);
}
/**
 * op_div - divide a by b
 * @a: integer
 * @b: integer
 * Return: quotient
 */

int op_div(int a, int b)
{
return (a / b);
}
/**
 * op_mod - modulo a by b
 * @a: integer
 * @b:integer
 * Return: remainder
 */
int op_mod(int a, int b)
{
return (a % b);
}
